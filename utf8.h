static inline uint32_t
decode(uint32_t* state, uint32_t* codep, uint32_t byte) {
  uint32_t type = utf8d[byte];

  *codep = (*state > UTF8_REJECT) ?
    (byte & 0x3fu) | (*codep << 6) :
    (0xff >> type) & (byte);

  *state = utf8d[256 + *state*16 + type];
  return *state;
}

typedef struct {
    const char* pos;
    uint32_t state;
    uint32_t codep;
    const char* codep_start;
    int codep_len;
} UTF8_Decoder;

UTF8_Decoder utf8_decode(const char* str) {
    UTF8_Decoder result;
    result.pos = str;
    result.state = 0;
    result.codep = 0;
    return result;
}

uint32_t utf8_next(UTF8_Decoder* dec) {
    while (1) {
        const char* pos = dec->pos++;
        unsigned char c = *pos;
        if (!c) return 0;

        if (c <= 0x7F) {
            dec->codep_start = pos;
            dec->codep_len = 1;
            return c;
        }

        uint32_t state0 = dec->state;
        switch(decode(&dec->state, &dec->codep, c)) {
            case UTF8_ACCEPT:
                return dec->codep;

            case UTF8_REJECT:
                fprintf(stderr, "ERROR: Got invalid utf8 sequence\n");
                return 0;

            default:
                if (state0 == UTF8_ACCEPT) {
                    dec->codep_start = pos;
                    dec->codep_len = dec->state / 3 + 2;
                }
                break;
        }
    }
}
